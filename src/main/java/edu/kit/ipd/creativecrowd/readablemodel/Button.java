package edu.kit.ipd.creativecrowd.readablemodel;

/**
 * Represents the presence of different buttons a worker can click on to traverse his taskconstellation/ his assignment
 */
public enum Button {

	/** The Start-Button (on Preview-Page). */
	Start,
	/** The Next-Button (to switch from one Task to the next */
	Next,
	/** The Again-Button (to repeat the current Task (give another Answer to the same CreativeTask) */
	Again,
	/** The Submit-Button (to submit the own work) */
	Submit

}
